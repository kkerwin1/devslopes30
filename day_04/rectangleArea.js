/**
 * Return the value of a rectangle's area.
 * @param {Number} x - value of rectangle's x dimension
 * @param {Number} y - value of rectangle's y dimension
 * @returns {Number} area - rectangle's area
 */
function calculateRectangleArea(x, y) {
    area = x * y;
    return area;
}

console.log(calculateRectangleArea(5, 6));
console.log(calculateRectangleArea(4, 7));